// Group winners by category
export function getWinnersByCategory() {
  return WINNERS.reduce((acc, w) => {
    if (!acc[w.category]) acc[w.category] = []
    acc[w.category].push(w)
    return acc
  }, {})
}

// Group winners by project within a category
export function getProjectsInCategory(category) {
  const winners = WINNERS.filter(w => w.category === category)
  return winners.reduce((acc, w) => {
    if (!acc[w.project]) acc[w.project] = []
    acc[w.project].push(w)
    return acc
  }, {})
}

// Get initials for avatar
export function getInitials(name) {
  return name.split(' ').slice(0, 2).map(n => n[0]).join('').toUpperCase()
}

